				<h2>Prasada</h2>
				<p class="subText">His holiness Dr. Shivaratri Rajendra Mahaswamiji decided to bring out a periodical ‘Prasada’ way back in 1967 basically to propagate Dharma, Darshana, Vedanta, culture, history, arts and other issues. Since 1985, it is being published once in two months. The periodical publishes works of several renowned scholars. ‘Prasada’ has readership all over the state with 6899 subscribers. During 2010-11, 6 issues have been published. </p>
				<p class="subText_clr">Other Publications</p>
				<p class="subText_clr">Jagadguru Sri Shivarathreeshwara Granthamale/Publications</p>
				<p class="subText">The 22nd Jagadguru Mantra Maharshi His Holiness Sri Shivarathri Mahaswamiji believed in social change through literature. To fulfill this objective, he started Jagadguru Sri Shivarathreeshwara Granthamale/ publications. So far, over 100 valuable titles are published. During the year, apart from books, calendars (pocket, wall and table) and panchangas were also published.</p>
				<p class="subText_clr">JSS Book House</p>
				<p class="subText">In order to reach the publications of Sri Shivarathreeshwara Granthamale and other good titles published by other publishers to the readers, JSS Book House retail outlet has been set up. The book house also supplies note books to JSS schools at a concessional rate.</p>
				<p class="subText_clr">Publications</p>
				<p class="subText">Under the “Sharana Samskrithi” Series, books written by several stalwarts in Kannada literature are being published by the Publications Division of the JSSMVP.</p>
				<p class="subText_clr">Periodicals</p>
				<p class="subText_clr">Sharanapatha</p>
				<p class="subText">Since 1990 Sharanapatha, a bi-annual English periodical is being published. The objective of this English publication is basically to introduce Indian culture, vachana literature, dharma and philosophy to other language readers. This has 3478 subscribers all over India and two issues have been published during the current year.</p>
				<p class="subText_clr">JSS Monthly</p>
